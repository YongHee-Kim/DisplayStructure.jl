@testset "DisplayArray" begin

    io = IOBuffer()

    array = DS.DisplayArray(10, 40, background='.')
    DS.render(io, array)
    @test String(take!(io)) ==
        "........................................\n" *
        "........................................\n" *
        "........................................\n" *
        "........................................\n" *
        "........................................\n" *
        "........................................\n" *
        "........................................\n" *
        "........................................\n" *
        "........................................\n" *
        "........................................\n"

    array[6, 8] = '噢'
    DS.render(io, array)
    @test String(take!(io)) ==
        "........................................\n" *
        "........................................\n" *
        "........................................\n" *
        "........................................\n" *
        "........................................\n" *
        ".......噢...............................\n" *
        "........................................\n" *
        "........................................\n" *
        "........................................\n" *
        "........................................\n"

    array[4:6, 8:19] = "這是一段\n非常感性的\n字串"
    DS.render(io, array)
    @test String(take!(io)) ==
        "........................................\n" *
        "........................................\n" *
        "........................................\n" *
        ".......這是一段.........................\n" *
        ".......非常感性的.......................\n" *
        ".......字串.............................\n" *
        "........................................\n" *
        "........................................\n" *
        "........................................\n" *
        "........................................\n"

end
